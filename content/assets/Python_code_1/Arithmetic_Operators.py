#Textbook p057 算數運算子
a = 23
b = 10

print('a =', a)
print('b =', b)

print('\na + b = %d'%(a + b)) #加法
print('a - b = %d'%(a - b)) #減法
print('a * b = %d'%(a * b)) #乘法
print('a / b = %.1f'%(a / b)) #除法
print('a // b = %d'%(a // b)) #除法，取商數
print('a %% b = %d'%(a % b)) #除法，取餘數

print('\na^2 = %d\nb^4 = %d'%(a**2, b**4)) #指數
