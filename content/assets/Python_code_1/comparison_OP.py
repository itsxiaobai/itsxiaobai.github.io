# 比較運算子 Comparison Operators

'''
>	大於 		greater than
<	小於 		less than
>=	大於等於 	greater or equal to 
<=	小於等於		less or equal to
==	等於 		equal to
!=	不等於		not equal to
'''

print(3 > 2)	#True
print(3 < 2)	#False
print(3 >= 2)	#True
print(3 <= 2)	#False
print(3 == 2)	#False
print(3 != 2)	#True

